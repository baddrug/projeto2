    private void bntEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntEditarActionPerformed
        // TODO add your handling code here:
         modoTela = "Editar";
        
        //Verifico se há linhas para poder editar
        if(jTable1.getRowCount()>0)
        {
            //Verifico se o usuário selecionou alguma linha (Primeira linha = 0) | Nada selecionado = -1)
            if(jTable1.getSelectedRow()>=0)
            {
                HabilitarFormulario();
                
                //Variável acessória para identifcar se o formulário está em modo de edição ou alteração
                modoTela = "Editar";

                //Atribuo os valores que estão na linha selecionada para a tabela
                jblIDU.setText(jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 0).toString());
                jtUser.setText(jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 1).toString());
                jtSenha.setText(jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 2).toString());
                jtSenhaa.setText(jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 3).toString());
                
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Selecione um usuário para editar!");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Não há usuário para editar!");
        }
    }//GEN-LAST:event_bntEditarActionPerformed

    private void bntRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntRemoverActionPerformed
        // TODO add your handling code here:
        if(jTable1.getRowCount()>0)
        {
            //Resgato o número da linha pelo JTable
            int numeroLinha = jTable1.getSelectedRow();
            
            //Resgato o ID (oculto) do cliente pelo JTableModel
            int IDusuario = Integer.parseInt(jTable1.getModel().getValueAt(numeroLinha, 0).toString());
            
            //Realizo a exclusão do cliente pelo ID
            if(UsuarioController.excluirUsuario(IDusuario))
            {
                //Recarrego a tabela, consultando diretamente do banco de dados
                this.CarregarTabela();
                JOptionPane.showMessageDialog(this,"Usuário excluído da base de dados");
            }else{
                JOptionPane.showMessageDialog(this,"Falha ao excluir o usuário!");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Não há usuário para excluir!");
        }
        
        this.DesabilitarFormulario();
        this.LimparFormulario();
    }//GEN-LAST:event_bntRemoverActionPerformed

