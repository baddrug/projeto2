    private void bntRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntRemoverActionPerformed
        // TODO add your handling code here:
        if(jTable1.getRowCount()>0)
        {
            //Resgato o número da linha pelo JTable
            int numeroLinha = jTable1.getSelectedRow();
            
            //Resgato o ID (oculto) do cliente pelo JTableModel
            int IDfuncionario = Integer.parseInt(jTable1.getModel().getValueAt(numeroLinha, 0).toString());
            
            //Realizo a exclusão do cliente pelo ID
            if(FuncionarioController.excluirFuncionario(IDfuncionario))
            {
                //Recarrego a tabela, consultando diretamente do banco de dados
                this.CarregarTabela();
                JOptionPane.showMessageDialog(this,"Funcionário excluído da base de dados");
            }else{
                JOptionPane.showMessageDialog(this,"Falha ao excluir o funcionário!");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Não há funconário para excluir!");
        }
        
        this.DesabilitarFormulario();
        this.LimparFormulario();
    }//GEN-LAST:event_bntRemoverActionPerformed

    private void bntSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntSalvarActionPerformed
        // TODO add your handling code here:
        if(modoTela.equals("Criar"))
            {
                //Passo ao controller a entrada do usuário e peço para salvar no banco de dados
                if(FuncionarioController.salvarFuncionario(jtNome.getText(),jfCPF.getText(), txtDtNasc.getText(),jtEndereco.getText(), jtBairro.getText(), jtNumero.getText(), jfTelefone.getText()))
                {
                    //Recarrego a tabela com os dados resgatados do banco de dados
                    this.CarregarTabela();
                    
                    JOptionPane.showMessageDialog(null,"Funcionário cadastrado com sucesso!");
                    LimparFormulario();
                }else{
                    JOptionPane.showMessageDialog(null,"Falha ao cadastrar Funcionário!");
                }

            }else
            {

                //Passo ao controller a entrada do usuário e peço para atualizar o banco de dados
                if(FuncionarioController.atualizarFuncionario(Integer.parseInt(jblIDF.getText()), jtNome.getText(),txtDtNasc.getText(), jtBairro.getText(),jfCPF.getText(), jtNumero.getText(), jtEndereco.getText(), jfTelefone.getText()))
                {
                    //Recarrego a tabela com os dados resgatados do banco de dados
                    this.CarregarTabela();
                    JOptionPane.showMessageDialog(this,"Funcionário atualizado com sucesso!");
                }else{
                    JOptionPane.showMessageDialog(this,"Falha ao atualizar dados do Funcionário!");
                }

            }

            LimparFormulario();
            DesabilitarFormulario();
    }//GEN-LAST:event_bntSalvarActionPerformed

