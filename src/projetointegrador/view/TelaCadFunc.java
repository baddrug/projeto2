     public void CarregarTabela(){
        
        //Peço ao controller resgatar os clientes do banco de dados
        ArrayList<String[]> linhasFuncionario = FuncionarioController.consultarFuncionario();
        
        //Opção 1 - Crio manualmente um modelo para a tabela e defino as colunas
        DefaultTableModel tmFuncionario = new DefaultTableModel();
        tmFuncionario.addColumn("ID");
        tmFuncionario.addColumn("Nome");
        tmFuncionario.addColumn("CPF");
        tmFuncionario.addColumn("Idade");
        tmFuncionario.addColumn("Endereço");
        tmFuncionario.addColumn("Bairro");
        tmFuncionario.addColumn("Número");
        tmFuncionario.addColumn("Telefone");
        
        jTable1.setModel(tmFuncionario);
        
        //Removo a coluna da View (JTable) mas mantenho na model para armazenar o ID
        jTable1.removeColumn(jTable1.getColumnModel().getColumn(0));
        
        //Para cada cliente resgatado do banco de dados, atualizo a tabela
        for(String[] f:linhasFuncionario)
        {
            tmFuncionario.addRow(f);
        }
        
        //Defino o tamanho para cada coluna
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(300);
        jTable1.getColumnModel().getColumn(1).setPreferredWidth(100);
    }
    

